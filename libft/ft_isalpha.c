/* 
** LIBRARY: <ctype.h>
** SYNOPSIS: alphabetic character test
** DESCRIPTION:	The isalpha() function tests for any
**				character for which isupper(3) or islower(3) is true.
*/

int	ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}
