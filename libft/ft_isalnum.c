/* 
** LIBRARY: <ctype.h>
** SYNOPSIS: alphanumeric character test
** DESCRIPTION: The isalnum() function tests for any 
** 				character for which isalpha or isdigit is true.
*/

int	ft_isalnum(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z')
		|| (c >= '0' && c <= '9'))
		return (1);
	return (0);
}
